SELECT
  DATE_ADD(NOW(), INTERVAL 1 DAY),
  DATE_ADD(NOW(), INTERVAL 1 YEAR),
  DATE_ADD(NOW(), INTERVAL -1 YEAR),
  DATE_SUB(NOW(), INTERVAL 1 YEAR),
  DATE_SUB(NOW(), INTERVAL -1 YEAR),
  DATEDIFF('2024-01-01', '2024-02-02'),
  DATEDIFF('2024-02-02', '2024-01-01'),
  DATEDIFF('2024-02-02 09:00', '2024-01-01 17:00'),
  TIMEDIFF('09:00', '09:02'),
  TIME_TO_SEC('09:00'),
  TIME_TO_SEC('09:00') - TIME_TO_SEC('09:02')